export const colorCode = (color) => {
  let colorIndex = COLORS.indexOf(color);
  if (colorIndex === -1) {
    throw new Error("Invalid transisctor color");
  }
  return colorIndex;
};

export const COLORS = [
  "black",
  "brown",
  "red",
  "orange",
  "yellow",
  "green",
  "blue",
  "violet",
  "grey",
  "white",
];
